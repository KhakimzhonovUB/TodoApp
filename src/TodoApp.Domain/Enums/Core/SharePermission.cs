namespace TodoApp.Domain.Enums;

/// <summary>
/// Перечисление прав доступа при совместном использовании списков задач
/// </summary>
public enum SharePermission
{
    /// <summary>
    /// Только чтение - пользователь может просматривать список и задачи, но не может их изменять
    /// </summary>
    ReadOnly = 0,
    
    /// <summary>
    /// Полный доступ - пользователь может просматривать, создавать, редактировать и удалять задачи
    /// </summary>
    FullAccess = 1
}
